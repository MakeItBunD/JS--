let i = 0;

while (i != 10) {
    i += 0.2;
}
// Потому что i не будет равно 10
// ближайшие значения к 10 будут
// 9.999999999999996, 10.199999999999996
// Именно поэтому цикл while (i != 10) будет бесконечный

// Код для проверки получившихся чисел.
// Он и доказывает неравенство 10
while (i < 13) {
    i += 0.2;
    console.log(i)
}